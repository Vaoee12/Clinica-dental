-- TABLA: AFILIADO 

CREATE TABLE AFILIADO(
    NUM_A           INTEGER       NOT NULL,
    TIPOAFILIADO    VARCHAR(15)    NOT NULL,
    COSTO_M         NUMERIC(6, 2)    NOT NULL,
    CONSTRAINT PK_AFILIADO PRIMARY KEY (NUM_A),
    CONSTRAINT CHK_TIPO_AFILIADO CHECK (TIPOAFILIADO IN ('INDIVIDUAL', 'GRUPAL')),
    CONSTRAINT CHK_COSTO_AFILIADO CHECK (COSTO_M >= 0)
);

-- TABLA: CONSULTORIO 

CREATE TABLE CONSULTORIO(
    NUM_COS         INTEGER       NOT NULL,
    ID_SUCURSAL     INTEGER       NOT NULL,
    CONSTRAINT PK_CONSULTORIO PRIMARY KEY (NUM_COS, ID_SUCURSAL)
);

-- TABLA: SUCURSAL 

CREATE TABLE SUCURSAL(
    ID_SUCURSAL     INTEGER       NOT NULL,
    NOMBRE_S        VARCHAR(40)    NOT NULL,
    CALLE           VARCHAR(40)    NOT NULL,
    NUMERO          INTEGER        NOT NULL,
    CIUDAD          VARCHAR(30)    NOT NULL,
   -- TRATAMIENTO_SUC VARCHAR(20)    NOT NULL,
    CONSTRAINT PK_SUCURSAL PRIMARY KEY (ID_SUCURSAL)
);

-- TABLA: CONSULTA 

CREATE TABLE CONSULTA(
    NUM_CON         INTEGER	        NOT NULL,
    FECHA_C         TIMESTAMP		NOT NULL,
    ESTATUS_C       VARCHAR(10)     NOT NULL,
    NUM_COS         INTEGER,
    ID_SUCURSAL     INTEGER,
    ID_CEDULA       INTEGER,
    CONSTRAINT PK_CONSULTA PRIMARY KEY (NUM_CON),
    CONSTRAINT CHK_ESTATUS_CONSULTA CHECK (ESTATUS_C IN ('ACTIVA', 'FINALIZADA', 'CANCELADA'))
);

-- TABLA: CONVENIO 

CREATE TABLE CONVENIO(
    NOMBRE_CONV     VARCHAR(30)    NOT NULL,
    REBAJA          INTEGER       NOT NULL,
    PERIODO         INTEGER       NOT NULL,
    CANT_PAC        INTEGER       NOT NULL,
    CONSTRAINT PK_CONVENIO PRIMARY KEY (NOMBRE_CONV),
    CONSTRAINT CHK_REBAJA CHECK (REBAJA BETWEEN 0 AND 100),
    CONSTRAINT CHK_PERIODO CHECK (PERIODO IN (6, 12)),
    CONSTRAINT CHK_CANT_PAC CHECK (CANT_PAC > 0)
);

-- TABLA: GRUPAL 

CREATE TABLE GRUPAL(
    NUM_A           INTEGER       NOT NULL,
    NOMBRE_CONV     VARCHAR(30),
    CONSTRAINT PK_GRUPAL PRIMARY KEY (NUM_A)
);

-- TABLA: INDIVIDUAL 

CREATE TABLE INDIVIDUAL(
    NUM_A           INTEGER       NOT NULL,
    FECHA_I         TIMESTAMP        NOT NULL,
    FECHA_F         TIMESTAMP        NOT NULL,
    CONSTRAINT PK_INDIVIDUAL PRIMARY KEY (NUM_A),
    CONSTRAINT CHK_FECHAS_INDIV CHECK (FECHA_F > FECHA_I)
);

-- TABLA: PACIENTE 

CREATE TABLE PACIENTE(
    ID_CEDULA       INTEGER       NOT NULL,
    NOMP            VARCHAR(30)    NOT NULL,
    APP             VARCHAR(30)    NOT NULL,
    AMP             VARCHAR(30)    NOT NULL,
    MONTO_MENSUAL   NUMERIC(6, 2)  NOT NULL,
    NUM_A           INTEGER,
    CONSTRAINT PK_PACIENTE PRIMARY KEY (ID_CEDULA),
    CONSTRAINT CHK_MONTO_MENSUAL CHECK (MONTO_MENSUAL >= 0)
);

-- TABLA: ODONTOLOGO 

CREATE TABLE ODONTOLOGO(
    CEDULA          INTEGER      NOT NULL,
    HORARIO_CON     TIMESTAMP       NOT NULL,
    HORARIO_SUC     TIMESTAMP       NOT NULL,
    ESPECIALIDAD    VARCHAR(30)    NOT NULL,
    NOMO            VARCHAR(30)    NOT NULL,
    APO             VARCHAR(30)    NOT NULL,
    AMO             VARCHAR(30),
    NUM_COS         INTEGER,
    ID_SUCURSAL     INTEGER,
    CONSTRAINT PK_ODONTOLOGO PRIMARY KEY (CEDULA)
);

-- TABLA: HISTORIAL 

CREATE TABLE HISTORIAL(
    NUM_CON         INTEGER       NOT NULL,
    CEDULA          INTEGER       NOT NULL,
    FECHA_IT        TIMESTAMP            NOT NULL,
    FECHA_FT        TIMESTAMP            NOT NULL,
    ID_PAGO         INTEGER,
    NOM_T           VARCHAR(10),
    CONSTRAINT PK_HISTORIAL PRIMARY KEY (NUM_CON, CEDULA),
    CONSTRAINT CHK_FECHAS_HIST CHECK (FECHA_FT >= FECHA_IT)
);

-- TABLA: TRATAMIENTO 

CREATE TABLE TRATAMIENTO(
    NOM_T           VARCHAR(10)    NOT NULL,
    --MONTO_MEN_SUC   NUMERIC(6, 2)    NOT NULL,
    COSTO           NUMERIC(6, 2)    NOT NULL,
    CONSTRAINT PK_TRATAMIENTO PRIMARY KEY (NOM_T),
    CONSTRAINT CHK_COSTO_TRAT CHECK (COSTO >= 0)
);

-- TABLA: REALIZA 

CREATE TABLE REALIZA(
    CEDULA          INTEGER       NOT NULL,
    NOM_T           VARCHAR(10)    NOT NULL,
    CONSTRAINT PK_REALIZA PRIMARY KEY (CEDULA, NOM_T)
);

-- TABLA: TELEFONO_P 

CREATE TABLE TELEFONO_P(
    TELEFONO        BIGINT      NOT NULL,
    ID_CEDULA       INTEGER       NOT NULL,
    CONSTRAINT PK_TELEFONO_P PRIMARY KEY (TELEFONO, ID_CEDULA)
);

-- TABLA: PAGO 

CREATE TABLE PAGO(
    ID_PAGO         INTEGER	       NOT NULL,
    FECHA_PAGO      TIMESTAMP        NOT NULL,
    MONTO           NUMERIC(6, 2)    NOT NULL,
    CONSTRAINT PK_PAGO PRIMARY KEY (ID_PAGO),
    CONSTRAINT CHK_MONTO_PAGO CHECK (MONTO >= 0)
);

-- TABLA: CONSULTA COMPLETA

CREATE TABLE CONSULTA_COMPLETA (
