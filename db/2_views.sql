CREATE OR REPLACE VIEW vista_sucursales_tratamientos AS
SELECT
    S.ID_SUCURSAL,
    S.NOMBRE_S AS SUCURSAL,
    S.CALLE || ' #' || S.NUMERO::TEXT || ', ' || S.CIUDAD AS DIRECCION,
    T.NOM_T AS TRATAMIENTO,
    T.COSTO
FROM SUCURSAL S
JOIN CONSULTORIO C ON S.ID_SUCURSAL = C.ID_SUCURSAL
JOIN ODONTOLOGO O ON O.NUM_COS = C.NUM_COS AND O.ID_SUCURSAL = C.ID_SUCURSAL
JOIN REALIZA R ON O.CEDULA = R.CEDULA
JOIN TRATAMIENTO T ON R.NOM_T=T.NOM_T;


