CREATE OR REPLACE VIEW pagos_mensuales_afiliados AS
    SELECT
        A.NUM_A AS NUMERO_ASOCIADO,
        EXTRACT(YEAR FROM P.FECHA_PAGO) AS ANO,
        EXTRACT(MONTH FROM P.FECHA_PAGO) AS MES,
        SUM(P.MONTO) AS MONTO_TOTAL
    FROM PAGO P
    JOIN HISTORIAL H ON P.ID_PAGO = H.ID_PAGO
    JOIN CONSULTA C ON C.NUM_CON = H.NUM_CON
    JOIN PACIENTE PA ON PA.ID_CEDULA = C.ID_CEDULA
    JOIN AFILIADO A ON A.NUM_A = PA.NUM_A
GROUP BY A.NUM_A, EXTRACT(YEAR FROM P.FECHA_PAGO), EXTRACT(MONTH FROM P.FECHA_PAGO);

