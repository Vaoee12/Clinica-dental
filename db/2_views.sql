CREATE OR REPLACE VIEW vista_monto_mensual_tratamiento_sucursal AS
    SELECT
        T.NOM_T AS TRATAMIENTO,
        S.NOMBRE_S AS SUCURSAL,
        EXTRACT(MONTH FROM P.FECHA_PAGO) AS MES,
        EXTRACT(YEAR FROM P.FECHA_PAGO) AS ANIO,
        SUM(P.MONTO) AS MONTO_MENSUAL
    FROM PAGO P
    JOIN HISTORIAL H ON P.ID_PAGO = H.ID_PAGO
    JOIN CONSULTA C ON H.NUM_CON = C.NUM_CON
    JOIN SUCURSAL S ON C.ID_SUCURSAL = S.ID_SUCURSAL
    JOIN TRATAMIENTO T ON H.NOM_T = T.NOM_T
    GROUP BY
        T.NOM_T,
        S.NOMBRE_S,
        EXTRACT(YEAR FROM P.FECHA_PAGO),
        EXTRACT(MONTH FROM P.FECHA_PAGO);

