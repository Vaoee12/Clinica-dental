CREATE OR REPLACE VIEW afiliado_tratamiento AS
    SELECT
        P.NOMP || ' ' || P.APP AS NOMBRE_AFILIADO,
        T.NOM_T AS Nombre_tratamiento,
        T.COSTO AS Costo,
        S.NOMBRE_S AS Sucursal,
        S.CALLE || ' #' || S.NUMERO || ', ' || S.CIUDAD AS DIRECCION,
        H.FECHA_IT,
        H.FECHA_FT
    FROM AFILIADO A
    JOIN PACIENTE P ON A.NUM_A=P.NUM_A
    JOIN CONSULTA C ON P.ID_CEDULA=C.ID_CEDULA
    JOIN HISTORIAL H ON C.NUM_CON = H.NUM_CON
    JOIN TRATAMIENTO T ON H.NOM_T=T.NOM_T
    JOIN SUCURSAL S ON C.ID_SUCURSAL=S.ID_SUCURSAL;


