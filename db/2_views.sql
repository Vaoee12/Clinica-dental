CREATE OR REPLACE VIEW viewodontologos AS
    SELECT
    O.CEDULA,
    O.NOMO || ' ' || O.APO AS NOMBRE_ODONTOLOGO,
    O.ESPECIALIDAD,
    T.NOM_T AS NOMBRE_TRATAMIENTO,
    S.NOMBRE_S AS SUCURSAL
    FROM ODONTOLOGO O
    JOIN CONSULTA C ON C.NUM_COS = O.NUM_COS AND C.ID_SUCURSAL = O.ID_SUCURSAL
    JOIN SUCURSAL S ON S.ID_SUCURSAL=C.ID_SUCURSAL
    JOIN REALIZA R ON O.CEDULA = R.CEDULA
    JOIN TRATAMIENTO T ON R.NOM_T=T.NOM_T;

