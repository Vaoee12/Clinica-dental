CREATE OR REPLACE VIEW horarioxsucursal AS
    SELECT
        O.NOMO || ' ' || O.APO AS NOMBRE_ODONTOLOGO,
        S.NOMBRE_S AS SUCURSAL,
        TO_CHAR(O.HORARIO_CON, 'HH24:MI') AS HORARIO_CONSULTORIO,
        TO_CHAR(O.HORARIO_SUC, 'HH24:MI') AS HORARIO_SUCURSAL
    FROM ODONTOLOGO O
    JOIN SUCURSAL S ON O.ID_SUCURSAL = S.ID_SUCURSAL;


